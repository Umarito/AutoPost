namespace Domain.Enums;

/// <summary>
/// Роль участника внутри рабочего пространства.
/// Роль является основой RBAC-модели и определяет, какие действия пользователь может выполнять
/// в рамках конкретной команды или организации.
/// </summary>
public enum WorkspaceRole
{
    /// <summary>
    /// Владелец рабочего пространства с полным контролем над доступом, настройками и подпиской.
    /// </summary>
    Owner,

    /// <summary>
    /// Администратор, управляющий участниками команды и подключенными социальными аккаунтами.
    /// </summary>
    Admin,

    /// <summary>
    /// Редактор, который создает, изменяет и планирует публикации, но не управляет всем workspace.
    /// </summary>
    Editor,

    /// <summary>
    /// Наблюдатель с доступом только к просмотру данных, аналитики и коммуникаций без права изменения.
    /// </summary>
    Viewer
}
