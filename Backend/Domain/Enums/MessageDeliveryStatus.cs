namespace Domain.Enums;

/// <summary>
/// Стадия доставки исходящего сообщения.
/// Позволяет отслеживать, как платформа обработала ответ команды или автоматизации.
/// </summary>
public enum MessageDeliveryStatus
{
    /// <summary>
    /// Сообщение отправлено в API платформы и ожидает подтверждения.
    /// </summary>
    Sending,

    /// <summary>
    /// Платформа подтвердила успешную отправку сообщения.
    /// </summary>
    Sent,

    /// <summary>
    /// Сообщение дошло до получателя на уровне платформы.
    /// </summary>
    Delivered,

    /// <summary>
    /// Платформа зафиксировала, что получатель открыл или прочитал сообщение.
    /// </summary>
    Read,

    /// <summary>
    /// Попытка отправки завершилась ошибкой.
    /// </summary>
    Failed
}
