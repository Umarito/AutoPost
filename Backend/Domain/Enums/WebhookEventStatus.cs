namespace Domain.Enums;

/// <summary>
/// Состояние обработки входящего webhook-события.
/// Позволяет отделить момент получения внешнего уведомления от фактической бизнес-обработки.
/// </summary>
public enum WebhookEventStatus
{
    /// <summary>
    /// Событие принято системой и сохранено, но обработка еще не началась.
    /// </summary>
    Received,

    /// <summary>
    /// Событие находится в процессе фоновой обработки.
    /// </summary>
    Processing,

    /// <summary>
    /// Событие обработано успешно, и полезная бизнес-реакция уже создана.
    /// </summary>
    Processed,

    /// <summary>
    /// Обработка завершилась неуспешно после допустимых попыток.
    /// </summary>
    Failed,

    /// <summary>
    /// Событие не должно обрабатываться, например из-за невалидной подписи или дубля.
    /// </summary>
    Ignored
}
