namespace Domain.Enums;

/// <summary>
/// Текущее состояние подключенного социального аккаунта.
/// Статус помогает определить, можно ли публиковать контент, принимать сообщения
/// и выполнять автоматизацию без повторной авторизации пользователя.
/// </summary>
public enum SocialAccountStatus
{
    /// <summary>
    /// Аккаунт подключен корректно, токены действительны и интеграция доступна для работы.
    /// </summary>
    Active,

    /// <summary>
    /// Токены устарели или более не подходят для обращения к API, поэтому требуется переподключение.
    /// </summary>
    TokenExpired,

    /// <summary>
    /// Аккаунт был отключен пользователем вручную внутри системы.
    /// </summary>
    Disconnected,

    /// <summary>
    /// Платформа отозвала доступ приложению или пользователь удалил разрешение на интеграцию.
    /// </summary>
    Revoked
}
