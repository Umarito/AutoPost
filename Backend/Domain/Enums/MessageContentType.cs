namespace Domain.Enums;

/// <summary>
/// Тип содержимого сообщения в inbox.
/// Помогает корректно отрисовать сообщение, определить доступные действия и понять,
/// какой формат данных пришел от платформы или был отправлен наружу.
/// </summary>
public enum MessageContentType
{
    /// <summary>
    /// Текстовое сообщение без отдельного медиа-вложения.
    /// </summary>
    Text,

    /// <summary>
    /// Сообщение с изображением.
    /// </summary>
    Image,

    /// <summary>
    /// Сообщение с видеоматериалом.
    /// </summary>
    Video,

    /// <summary>
    /// Сообщение с аудиоконтентом.
    /// </summary>
    Audio,

    /// <summary>
    /// Сообщение или элемент, связанный со story-форматом платформы.
    /// </summary>
    Story,

    /// <summary>
    /// Сообщение, содержащее стикер.
    /// </summary>
    Sticker,

    /// <summary>
    /// Сообщение-реакция без полноценного текстового тела.
    /// </summary>
    Reaction,

    /// <summary>
    /// Контент, тип которого система получила, но пока не умеет полноценно обрабатывать.
    /// </summary>
    Unsupported
}
