namespace Domain.Enums;

/// <summary>
/// Состояние публикации на одном конкретном целевом аккаунте.
/// Используется для детального отслеживания результата по каждой платформе отдельно.
/// </summary>
public enum TargetStatus
{
    /// <summary>
    /// Цель создана и ожидает запуска задачи публикации.
    /// </summary>
    Pending,

    /// <summary>
    /// Выполняется активное обращение к API платформы для размещения контента.
    /// </summary>
    Publishing,

    /// <summary>
    /// Контент успешно опубликован на целевой платформе.
    /// </summary>
    Published,

    /// <summary>
    /// Публикация на платформе завершилась ошибкой после исчерпания доступных попыток.
    /// </summary>
    Failed,

    /// <summary>
    /// Для цели уже назначена повторная попытка публикации.
    /// </summary>
    Retrying
}
