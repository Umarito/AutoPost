namespace Domain.Enums;

/// <summary>
/// Тарифный план рабочего пространства.
/// План определяет коммерческий уровень подписки организации и влияет на доступные лимиты,
/// функциональные возможности и масштаб использования платформы.
/// </summary>
public enum SubscriptionPlan
{
    /// <summary>
    /// Базовый бесплатный тариф с минимальными лимитами и ограниченным набором возможностей.
    /// </summary>
    Free,

    /// <summary>
    /// Профессиональный тариф для небольших команд, которым требуется регулярная публикация
    /// и базовые инструменты совместной работы.
    /// </summary>
    Pro,

    /// <summary>
    /// Бизнес-тариф для команд, активно использующих мультиплатформенную публикацию,
    /// inbox и автоматизацию взаимодействий.
    /// </summary>
    Business,

    /// <summary>
    /// Корпоративный тариф для крупных клиентов с повышенными лимитами и расширенными возможностями.
    /// </summary>
    Enterprise
}
