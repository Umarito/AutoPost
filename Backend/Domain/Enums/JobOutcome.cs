namespace Domain.Enums;

/// <summary>
/// Итог отдельной фоновой попытки публикации.
/// Значение описывает состояние конкретного запуска job, а не всей публикации целиком.
/// </summary>
public enum JobOutcome
{
    /// <summary>
    /// Фоновая задача еще выполняется и не достигла финального результата.
    /// </summary>
    InProgress,

    /// <summary>
    /// Текущий запуск задачи завершился успешно.
    /// </summary>
    Succeeded,

    /// <summary>
    /// Текущий запуск задачи завершился ошибкой.
    /// </summary>
    Failed,

    /// <summary>
    /// Задача завершилась с необходимостью повторного запуска по стратегии устойчивости.
    /// </summary>
    Retrying
}
