namespace Domain.Enums;

/// <summary>
/// Общий статус жизненного цикла публикации.
/// Значение агрегирует состояние поста во всех его целях публикации и отражает общий результат процесса.
/// </summary>
public enum PostStatus
{
    /// <summary>
    /// Публикация создана как черновик и еще не запущена в расписание.
    /// </summary>
    Draft,

    /// <summary>
    /// Публикация поставлена в очередь планировщика и ожидает времени отправки.
    /// </summary>
    Scheduled,

    /// <summary>
    /// В данный момент выполняется процесс публикации хотя бы на одну из целевых платформ.
    /// </summary>
    Publishing,

    /// <summary>
    /// Публикация успешно завершилась на всех целевых аккаунтах.
    /// </summary>
    Published,

    /// <summary>
    /// Публикация завершилась частично успешно: часть платформ отработала, часть завершилась ошибкой.
    /// </summary>
    PartiallyFailed,

    /// <summary>
    /// Ни одна из целевых публикаций не завершилась успехом.
    /// </summary>
    Failed,

    /// <summary>
    /// Публикация была отменена до начала фактической отправки на внешние платформы.
    /// </summary>
    Cancelled
}
