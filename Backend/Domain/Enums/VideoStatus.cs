namespace Domain.Enums;

/// <summary>
/// Стадия жизненного цикла загруженного видеофайла.
/// Статус отражает, можно ли использовать медиа в публикациях и требуется ли дополнительная обработка.
/// </summary>
public enum VideoStatus
{
    /// <summary>
    /// Файл еще загружается в хранилище, и передача данных не завершена.
    /// </summary>
    Uploading,

    /// <summary>
    /// Загрузка завершена, но система еще анализирует файл и извлекает технические метаданные.
    /// </summary>
    Processing,

    /// <summary>
    /// Видео полностью готово к использованию в постах и прошло базовую подготовку.
    /// </summary>
    Ready,

    /// <summary>
    /// Во время загрузки или обработки произошла ошибка, из-за которой файл нельзя использовать.
    /// </summary>
    Failed,

    /// <summary>
    /// Видео помечено на удаление и больше не должно участвовать в новых публикациях.
    /// </summary>
    Deleted
}
