namespace Domain.Enums;

/// <summary>
/// Оператор сравнения для условий автоматизации.
/// Используется вместе с типом условия, чтобы определить логику проверки входящего события.
/// </summary>
public enum ConditionOperator
{
    /// <summary>
    /// Проверяет, содержит ли значение искомую подстроку.
    /// </summary>
    Contains,

    /// <summary>
    /// Проверяет полное равенство значения.
    /// </summary>
    Equals,

    /// <summary>
    /// Проверяет, начинается ли значение с заданного фрагмента.
    /// </summary>
    StartsWith,

    /// <summary>
    /// Проверяет, оканчивается ли значение заданным фрагментом.
    /// </summary>
    EndsWith,

    /// <summary>
    /// Разрешает прохождение любого значения без дополнительной проверки.
    /// </summary>
    Any,

    /// <summary>
    /// Проверяет отсутствие указанной подстроки в значении.
    /// </summary>
    NotContains
}
