namespace Domain.Enums;

/// <summary>
/// Статус участия пользователя в рабочем пространстве.
/// Перечисление отражает, на каком этапе находится приглашение и активен ли доступ участника.
/// </summary>
public enum MemberStatus
{
    /// <summary>
    /// Приглашение отправлено, но пользователь еще не завершил присоединение к рабочему пространству.
    /// </summary>
    Invited,

    /// <summary>
    /// Пользователь принял приглашение и имеет действующий доступ согласно назначенной роли.
    /// </summary>
    Active,

    /// <summary>
    /// Доступ пользователя временно приостановлен без удаления исторических связей и аудита.
    /// </summary>
    Suspended
}
