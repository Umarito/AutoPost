namespace Domain.Enums;

/// <summary>
/// Статус записи в очереди отложенных личных сообщений.
/// Показывает, ожидает ли система подходящего момента для отправки или уже завершила работу с записью.
/// </summary>
public enum PendingDMStatus
{
    /// <summary>
    /// Запись ожидает повторной проверки условий отправки.
    /// </summary>
    Waiting,

    /// <summary>
    /// Отложенное сообщение в итоге было успешно отправлено.
    /// </summary>
    Sent,

    /// <summary>
    /// Срок ожидания истек, и сообщение больше не будет отправляться автоматически.
    /// </summary>
    Expired,

    /// <summary>
    /// Отложенная отправка была отменена вручную.
    /// </summary>
    Cancelled
}
