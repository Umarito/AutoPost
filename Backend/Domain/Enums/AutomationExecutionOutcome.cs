namespace Domain.Enums;

/// <summary>
/// Результат выполнения правила автоматизации по конкретному событию.
/// Значение используется для истории срабатываний, аналитики и защиты от повторного исполнения.
/// </summary>
public enum AutomationExecutionOutcome
{
    /// <summary>
    /// Все предусмотренные действия были выполнены успешно.
    /// </summary>
    Executed,

    /// <summary>
    /// Выполнение было осознанно пропущено из-за лимитов, дубликата или невыполненных условий.
    /// </summary>
    Skipped,

    /// <summary>
    /// Действие не было выполнено сразу и было перенесено в очередь ожидания.
    /// </summary>
    Pending,

    /// <summary>
    /// Во время выполнения возникла ошибка, из-за которой сценарий не завершился успешно.
    /// </summary>
    Failed
}
