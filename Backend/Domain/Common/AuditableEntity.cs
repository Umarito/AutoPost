namespace Domain.Common;

/// <summary>
/// Базовый тип для сущностей, жизненный цикл которых требует хранения даты создания
/// и даты последнего изменения на уровне доменной модели.
/// Класс применяется только там, где обе временные отметки действительно присутствуют
/// в техническом задании и отражают состояние предметного объекта.
/// </summary>
/// <typeparam name="TKey">
/// Тип идентификатора сущности. В доменном слое AutoPost используется <see cref="Guid" />.
/// </typeparam>
public abstract class AuditableEntity<TKey> : BaseEntity<TKey>
{
    /// <summary>
    /// Дата и время первоначального создания сущности.
    /// Для объектов планирования и автоматизации это значение фиксирует момент,
    /// когда объект впервые появился в рабочем пространстве системы.
    /// </summary>
    public DateTime CreatedAt { get; protected set; }

    /// <summary>
    /// Дата и время последнего изменения сущности.
    /// Поле позволяет отслеживать редактирование бизнес-объекта без обращения к журналам аудита
    /// и используется там, где состояние объекта может меняться после создания.
    /// </summary>
    public DateTime UpdatedAt { get; protected set; }
}
